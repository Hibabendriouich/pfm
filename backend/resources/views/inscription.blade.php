   <script>
    document.addEventListener('DOMContentLoaded', function() {
    const inscrireBtn = document.getElementById("ins");
    const emailInput = document.getElementById("email");
    const passwordInput = document.getElementById("password");
    const popupOverlay = document.getElementById('popup-overlay');
    const medicalForm = document.getElementById('medicalForm');

    // Show popup when clicking "Je m'inscris"
    inscrireBtn.addEventListener('click', function(event) {
        event.preventDefault(); // Prevent default behavior

        const email = emailInput.value.trim();
        const password = passwordInput.value.trim();

        // Validate email
        if (email === '') {
            alert("Veuillez entrer votre email.");
            return;
        } else if (!email.includes('@')) {
            alert("Veuillez respecter la forme d'un email.");
            return;
        }

        // Validate password
        if (password === '') {
            alert("Veuillez entrer un mot de passe.");
            return;
        }

        // If all validations pass, show the popup
        showPopup();
    });

    // Handle medical form submission
    medicalForm.addEventListener('submit', function(event) {
        event.preventDefault();

        // Récupérer les valeurs des champs
        const formData = {
            nom: document.getElementById("nom").value,
            dateNaissance: document.getElementById("date-naissance").value,
            telephone: document.getElementById("telephone").value,
            allergies: document.getElementById("allergies").value,
            antecedents: document.getElementById("antecedents").value,
            traitements: document.getElementById("traitements").value,
            fumeur: document.getElementById("fumeur").value,
            alcool: document.getElementById("alcool").value
        };

        // Sauvegarder les données dans le localStorage
        Object.entries(formData).forEach(([key, value]) => {
            localStorage.setItem(key, value);
        });

        // Message de confirmation
        alert("Vos informations ont été sauvegardées avec succès ! ");
        
        // Hide the popup
        hidePopup();
    });

    // Close popup when clicking outside
    popupOverlay.addEventListener('click', function(e) {
        if (e.target === popupOverlay) {
            hidePopup();
        }
    });

    function showPopup() {
        popupOverlay.style.display = 'block';
    }

    function hidePopup() {
        popupOverlay.style.display = 'none';
    }
});


    </script>
